export function getPosterHeight(posterSize) {
  switch (posterSize) {
    case '12 x 18 in':
    case '24 x 36 in':
      return 1350; // ratio 1.5
    case 'A2 poster':
      return 1273; // ratio 1.414 (594/420)
    case '18 x 24 in':
    default:
      return 1200; // ratio 1.333
  }
}

